    /**
     * 获取Term
     *
     * @param year      公历年
     * @param dayOffset 离1月1号的天数  从1开始  eg: 2014.1.1   dayoffset 0
     * @return
     */
    public static int getTerm(int year, int dayOffset) {
        int term = -1;
        int index = year - MIN;
        if (index >= 0 && index < TermTable.length / 24) {
            int begin = index * 24;
            term = searchValueInArray(TermTable, begin, begin + 23, dayOffset);
        }
        return term;
    }

    /**
     * 搜寻
     *
     * @param termTable
     * @param begin
     * @param end
     * @param dayOffset
     * @return
     */
    private static int searchValueInArray(int[] termTable, int begin, int end, int dayOffset) {
        end = Math.min(termTable.length, end);
        for (int i = begin; i <= end; i++) {
            if (termTable[i] == dayOffset) {
                return i - begin;
            }
        }
        return -1;
    }

    /**
     * @param year
     * @param term
     * @return
     */
    public static int getOffsetByTerm(int year, int term) {
        int index = year - MIN;
        if (index >= 0 && index < TermTable.length / 24) {
            int begin = index * 24 + term;
            return TermTable[begin];
        }
        return -1;
    }


    /**
     * 寻找。。。
     *
     * @param year
     * @param dayInYear
     * @return
     */
    public static int findPreTerm(int year, int dayInYear) {
        int index = year - MIN;
        if (index > 0 && index < TermTable.length / 24) {
            int begin = index * 24;
            return findPreTerm(TermTable, dayInYear, begin, 24);
        }
        return -1;
    }


    public static int findPreTerm(int[] termTable, int dayInYear, int begin, int len) {
        int value[] = new int[25];
        System.arraycopy(termTable, begin, value, 0, 24);
        value[24] = dayInYear;
        int index = Arrays.binarySearch(value, 0, 24, dayInYear);
        if (index > -1) {
            return index;
        }
        Arrays.sort(value);
        index = Arrays.binarySearch(value, dayInYear);
        return index - 1;
    }


